// model.dart
class Plant {
  final String label;
  final String resume;
  final String type;
  final String imageUrl;
  final String description;
  final String benefit;
  final String content;
  final String use;

  Plant({
    required this.label,
    required this.resume,
    required this.type,
    required this.imageUrl,
    required this.description,
    required this.benefit,
    required this.content,
    required this.use,
  });
}
